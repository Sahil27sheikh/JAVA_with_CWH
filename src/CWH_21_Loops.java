public class CWH_21_Loops {
    // Their are three types of loops in java
    public static void main(String[] args) {

        System.out.println("Using While Loop");
        int i = 1;
        while (i<=5) {
            System.out.println(i);
            i++;
        }
        System.out.println("Finish while loop");
    }
}
