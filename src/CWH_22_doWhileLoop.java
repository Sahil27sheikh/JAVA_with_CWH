public class CWH_22_doWhileLoop {
    public static void main(String[] args) {
        // the while loop is first check the programme and then execute.

        // do-while loop
        // in do while loop first is enter the programme and then check the condition.
        // it is the guaranteed to execute at least ones.
        int b = 0;
        do {
            System.out.println(b);
            b++;
        }
        while (b<+5);


        int c = 10;
        do {
            System.out.println(c);
            b++;
        }
        while (c<+5);


        // Quick Quiz
        // print the "n" number of do while loop
        int a = 1;
        do{
            System.out.println("yes "+a);
            a++;
        }
        while (a<=100);
    }
}
